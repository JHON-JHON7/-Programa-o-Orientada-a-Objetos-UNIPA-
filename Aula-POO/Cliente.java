class Cliente {
    String nome;
    String endereco;
    String cpf;
    double renda;
    int idCliente;

    // Construtor da classe Cliente
    public Cliente(String nome, String endereco, String cpf, double renda, int idCliente) {
        this.nome = nome;
        this.endereco = endereco;
        this.cpf = cpf;
        this.renda = renda;
        this.idCliente = idCliente;
    }
}
